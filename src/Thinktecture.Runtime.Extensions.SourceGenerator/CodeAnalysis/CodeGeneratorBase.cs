namespace Thinktecture.CodeAnalysis;

public abstract class CodeGeneratorBase
{
   internal const string GENERATED_CODE_PREFIX = @"// <auto-generated />
#nullable enable";

   internal static readonly string GENERATED_CODE_ATTRIBUTE;

   static CodeGeneratorBase()
   {
      var version = typeof(CodeGeneratorBase).Assembly.GetName().Version?.ToString() ?? "0.0.0.0";
      GENERATED_CODE_ATTRIBUTE = $"[global::System.CodeDom.Compiler.GeneratedCodeAttribute(\"Thinktecture.Runtime.Extensions.SourceGenerator\", \"{version}\")]";
   }

   public abstract string CodeGeneratorName { get; }
   public abstract string FileNameSuffix { get; }
   public abstract void Generate(CancellationToken cancellationToken);
}
