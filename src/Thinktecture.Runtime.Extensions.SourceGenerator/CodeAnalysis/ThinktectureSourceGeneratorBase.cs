using Microsoft.CodeAnalysis;

namespace Thinktecture.CodeAnalysis;

public abstract class ThinktectureSourceGeneratorBase
{
   private readonly string? _generatedFileSuffix;

   protected ThinktectureSourceGeneratorBase(string? generatedFileSuffix)
   {
      _generatedFileSuffix = generatedFileSuffix;
   }

   protected void EmitFile(SourceProductionContext context, INamespaceSymbol typeNamespace, string typeName, string? generatedCode)
   {
      if (!String.IsNullOrWhiteSpace(generatedCode))
      {
         context.AddSource($"{(typeNamespace.IsGlobalNamespace ? null : $"{typeNamespace}.")}{typeName}{_generatedFileSuffix}.g.cs", $@"// <auto-generated />
#nullable enable
{generatedCode}");
      }
   }
}
